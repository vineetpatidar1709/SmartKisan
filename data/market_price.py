def calculate_profit(crop_name, yield_per_acre, land_size=1):

    if crop_name not in market_data:
        return None

    data = market_data[crop_name]

    revenue = yield_per_acre * data["price_per_ton"] * land_size
    total_cost = data["input_cost_per_acre"] * land_size

    profit = revenue - total_cost

    return {
        "revenue": revenue,
        "cost": total_cost,
        "profit": profit,
        "demand": data["demand"],
        "volatility": data["volatility"]
    }